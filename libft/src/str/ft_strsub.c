#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*str;
	size_t	i;

	if (!s)
		return (NULL);
	i = 0;
	mallcheck(str = (char*)malloc(len + 1));
	while (i < len)
		str[i++] = s[start++];
	str[i] = '\0';
	return (str);
}
